package com.han.demo;

public class Demo4 {
	/**
	 * 关系运算
	 * @param args
	 */
public static void main(String[] args) {
	int a=10,b=20;
	System.out.println("a="+a+",b="+b);
	System.out.println("a>b表达是结果为："+(a>b));
	System.out.println("a<b表达是结果为："+(a<b));
	System.out.println("a>=b表达是结果为："+(a>=b));
	System.out.println("a<=b表达是结果为："+(a<=b));
	System.out.println("a==b表达是结果为："+(a==b));
	System.out.println("a！=b表达是结果为："+(a!=b));
}
}
