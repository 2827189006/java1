package com.han.demo;

public class Demo {
public static void main(String[] args) {
	 //一元运算自增自减赋值运算
	int num1=5,num2=5,sum1=num1++,sum2=++num2;
	System.out.println("unm1"+num1);
	System.out.println("sum1="+sum1);
	System.out.println("\nunm2="+num2);
	System.out.println("sum2="+sum2);
}
}
